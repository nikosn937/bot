    if pd.isna(keyword):
        return ''
    # 1. ÎœÎµÏ„Î±Ï„ÏÎ¿Ï€Î® ÏƒÎµ Ï€ÎµÎ¶Î¬, Î±Ï†Î±Î¯ÏÎµÏƒÎ· ÎºÎµÎ½ÏÎ½
